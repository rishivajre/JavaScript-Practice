// Switch Statement

let color = "blue";

switch(color) {
    case "red":
        console.log("Color is red");
        break;
    case "blue":
        console.log("Color is blue");
        break;
    case "green":
        console.log("Color is Green");
        break;
    default:
        console.log("Unknown Color");
}
